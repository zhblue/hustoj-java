package com.hustoj.entity;

public class Sim {
    private int sId;
    private Integer simSId;
    private Integer sim;

    public Sim() {}

    public int getsId() { return sId; }
    public void setsId(int sId) { this.sId = sId; }
    public Integer getSimSId() { return simSId; }
    public void setSimSId(Integer simSId) { this.simSId = simSId; }
    public Integer getSim() { return sim; }
    public void setSim(Integer sim) { this.sim = sim; }
}
